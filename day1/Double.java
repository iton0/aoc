package day1;

public class Double {
	int freq2;
	int freq1;

	Double(int freq1, int freq2) {
		this.freq1 = freq1;
		this.freq2 = freq2;
	}

	public int getFreq2() {
		return freq2;
	}

	public void setFreq2(int freq2) {
		this.freq2 = freq2;
	}

	public int getFreq1() {
		return freq1;
	}

	public void setFreq1(int freq1) {
		this.freq1 = freq1;
	}

}
